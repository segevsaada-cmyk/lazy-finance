async function main() {
  const tip = TIPS[dayOfYear() % TIPS.length];

  const url = `http://localhost:${PORT}/send`;

  try {
    const res = await fetch(url, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json', 'x-api-key': API_KEY },
      body: JSON.stringify({ phone: PHONE, message: tip }),
    });
    const data = await res.json();
    if (data.success) {
      console.log(`[${new Date().toISOString()}] ✅ Sent tip ${dayOfYear() % TIPS.length} to ${PHONE}`);
      process.exit(0);
    } else {
      console.error(`[${new Date().toISOString()}] ❌ Send failed:`, data);
      process.exit(1);
    }
  } catch (err) {
    console.error(`[${new Date().toISOString()}] ❌ Error:`, err.message);
    process.exit(1);
  }
}

main();
